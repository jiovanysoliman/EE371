// Module increments/decrements a counter based on the enter/exit signals and outputs to HEX_display module.
// Max occupancy in parking lot is 16.
// May need to change module header with appropriate I/O as necessary.
module car_counter(clk, reset, enter, exit, count);
	input  logic enter, exit;
	input  logic clk, reset;
	output logic [4:0] counter; // 5-bit signal for range 0-16. Does not exceed 16.
endmodule